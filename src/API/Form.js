export const sendFormDetails = async (data) => {
  return fetch(`https://form-to-pdf.herokuapp.com/sendPdfOnEmail`, {
    method: "POST",
    headers: {
      Accept: "application/json",
      "Content-Type": "application/json",
    },
    body: JSON.stringify(data),
  })
    .then((response) => {
      return response.json();
    })
    .catch((err) => {
      console.log(err);
    });
};
